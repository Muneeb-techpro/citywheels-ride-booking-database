 CurrentLocation Varchar(50),
 destinationLocation Varchar(50),
 VechileType Varchar(50),
 RideStatus Varchar(50),
 NumberOfSeats int,
 DriverEarning float,
 PassengerId int,
 DriverId int,
 foreign key(DriverId) references Driver(DriverId),
 foreign key(PassengerId) references Passenger(PassengerId) );



 -- Table 6
 
 Create Table FeedBack(ridingExperience Varchar(20),
 Suggestion Varchar(50),
 DriverPerformance Varchar(20),
 PassengerId int,
 foreign key(PassengerId) references Passenger(PassengerId) );
 
 
 -- Table 7

 Create table  RideHistoryRecord(RideHistoryRecordId int primary key,
 StartingTime DateTime,
 EndingTime DateTime,
 Location Varchar(100),
 PassengerId int,
 DriverId int,
 
